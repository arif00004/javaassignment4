public class Math {

    public static void main(String[] args) {
        int a = 10; // value 1
        int b = 2; // value 2
        int sum = (a + b); // Adding the defined values
        int multiplication = (a * b); // Multiplying the defined values
        int division = (a / b); // Dividing the defined values
        int subtraction = (a - b); //Subtracting the defined value

        // Providing result on defined objects
        System.out.println("Sum is:" +sum);
        System.out.println("Multiplication is:" +multiplication);
        System.out.println("Division is:" +division);
        System.out.println("Subtraction is:" +subtraction);
    }
}
